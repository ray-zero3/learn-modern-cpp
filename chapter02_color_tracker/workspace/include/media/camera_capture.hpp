// ============================================================
// media/camera_capture.hpp
// ------------------------------------------------------------
// カメラデバイスを RAII で管理するクラスの宣言。
//
// RAII (Resource Acquisition Is Initialization) とは:
//   コンストラクタでリソースを取得し、デストラクタで解放する。
//   オブジェクトのスコープがリソースの寿命になる。
//
// TS 対比:
//   TS では finally / try-with-resources がない代わりに、
//   C++ では「オブジェクトがスコープを外れたとき必ずdtorが走る」
//   という保証があるため、明示的なクリーンアップが不要。
//   TypeScript の Symbol.dispose (explicit resource management)
//   が近い概念だが、C++ では言語レベルで保証されている。
//
// 詳細: docs/deepdive/01-raii.md
// ============================================================
#pragma once

#include <optional>

// OpenCV のヘッダ。カメラ入力と Mat（画像行列）を使う。
#include <opencv2/videoio.hpp>
#include <opencv2/core/mat.hpp>

namespace media {

// ============================================================
// CameraCapture — cv::VideoCapture の RAII ラッパ
// ------------------------------------------------------------
// cv::VideoCapture を直接使うと:
//   - 例外が飛んだときにリリース忘れが起きる
//   - ムーブ/コピーの扱いが不明確になる
//
// このクラスは「カメラは1つのオブジェクトに1つだけ」を
// 強制し、寿命をスコープで管理できるようにする。
// ============================================================
class CameraCapture {
public:
    // デフォルトコンストラクタ: カメラを開かない状態で構築する。
    // open() を呼ぶまでは何もしない。
    CameraCapture() = default;

    // -------------------------------------------------------
    // コピー禁止
    // -------------------------------------------------------
    // カメラは物理デバイス。同じデバイスを2つの CameraCapture
    // オブジェクトが「所有」したら、どちらのデストラクタが
    // 先に走るかで競合が生じる（=二重解放 / use-after-free の原因）。
    //
    // TS 対比: TS にはコピーの「深さ」の概念が暗黙的。
    //   C++ では「コピーできない」を型で明示できる。
    CameraCapture(const CameraCapture&)            = delete;
    CameraCapture& operator=(const CameraCapture&) = delete;

    // -------------------------------------------------------
    // ムーブ可能
    // -------------------------------------------------------
    // 「所有権の移転」は許可する。
    //   ムーブ後の元オブジェクトは is_opened() == false になる
    //   (内部的に cv::VideoCapture がムーブされる)。
    //
    // TS 対比: TS では変数の再代入時に「元の参照が残る」が
    //   C++ のムーブは「所有権ごと移転 → 元は無効化」。
    //   Rust の move に近い概念。
    CameraCapture(CameraCapture&&)            = default;
    CameraCapture& operator=(CameraCapture&&) = default;

    // -------------------------------------------------------
    // デストラクタ: カメラを自動解放
    // -------------------------------------------------------
    // cv::VideoCapture::release() を呼ぶ。
    // スコープを外れたとき（例外発生時も含む）に必ず呼ばれる。
    ~CameraCapture();

    // -------------------------------------------------------
    // open: カメラデバイスを開く
    // -------------------------------------------------------
    // device_id: 0 = 内蔵カメラ, 1 = 外部カメラ, -1 は確実に失敗
    // 戻り値: 開けたら true, 失敗したら false
    //
    // [[nodiscard]]: 戻り値を無視したらコンパイル警告。
    //   TS で Promise を await しないのと似た問題を型レベルで防ぐ。
    [[nodiscard]] bool open(int device_id);

    // -------------------------------------------------------
    // read_frame: フレームを1枚読む
    // -------------------------------------------------------
    // 戻り値: std::optional<cv::Mat>
    //   成功 → 画像データが入った cv::Mat
    //   失敗 (未開放/EOFなど) → std::nullopt
    //
    // TS 対比: TS では null チェックで済むが、C++ では
    //   std::optional が「値があるかもしれない型」を明示する。
    //   undefined を返す可能性のある関数に型安全を与える仕組み。
    [[nodiscard]] std::optional<cv::Mat> read_frame();

    // カメラが現在開いているかどうかを返す。
    [[nodiscard]] bool is_opened() const;

private:
    // cv::VideoCapture は OpenCV のカメラハンドル。
    // PRIVATE にすることで、外部から直接触れないようにする。
    cv::VideoCapture cap_;
};

} // namespace media
