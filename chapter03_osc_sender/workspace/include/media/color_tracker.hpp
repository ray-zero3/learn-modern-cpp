// ============================================================
// media/color_tracker.hpp
// ------------------------------------------------------------
// HSV色域を使って特定色の物体を追跡するクラスの宣言。
//
// この章で新しく登場する概念:
//   - std::optional<T>: 結果が「ある/なし」の型
//   - std::span<const T>: 非所有ビュー（const 参照の汎化）
//   - std::ranges: パイプライン的な配列処理
//   - const T& vs const T: 参照渡しとコピーの使い分け
//
// TS 対比:
//   - std::optional<TrackResult> ≈ TrackResult | null
//   - const cv::Mat& ≈ Readonly<Mat> （所有しないビュー）
//   - HsvRange は TS の interface に相当するが、値型として扱う
// ============================================================
#pragma once

#include <optional>

#include <opencv2/core.hpp>

namespace media {

// ============================================================
// HsvRange — HSV 色空間での色域指定
// ------------------------------------------------------------
// HSV (Hue/Saturation/Value) の下限・上限を指定して色を絞る。
// cv::Scalar は float[4] のラッパ（OpenCV の汎用数値型）。
//
// HSV の範囲（OpenCV 式）:
//   H: 0-179 (実際の 0-360 度を半分にスケール)
//   S: 0-255
//   V: 0-255
// ============================================================
struct HsvRange {
    cv::Scalar lower; // HSV 下限値（例: {10, 100, 100} = 薄い赤）
    cv::Scalar upper; // HSV 上限値（例: {20, 255, 255} = 濃い橙）
};

// ============================================================
// TrackResult — 追跡結果
// ------------------------------------------------------------
// std::optional で包んで返す（検出失敗時は nullopt）。
// struct のメンバは値型（コピー可）。
// ============================================================
struct TrackResult {
    cv::Point2f centroid; // 重心座標 (pixel, float 精度)
    double area;          // 検出領域の面積 (pixel^2)
};

// ============================================================
// track: 指定色域の物体を追跡する
// ------------------------------------------------------------
// bgr   : カメラから取得した BGR 画像（cv::Mat）
//         const T& で渡すのは「コピーしない」宣言。
//         cv::Mat は内部で参照カウントを持つため、コピーは安価だが
//         明示的に「所有しない」を示す const& が適切。
//
// range : HSV の範囲指定。値が小さいので const& でも値渡しでも可だが
//         一貫性のため const& を使う。
//
// 戻り値: 物体が検出されたら TrackResult、なければ nullopt
// ============================================================
[[nodiscard]] std::optional<TrackResult> track(const cv::Mat& bgr,
                                                const HsvRange& range);

} // namespace media
